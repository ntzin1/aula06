let temperatura = 35;  

// 2. Estrutura condicional if-else
if (temperatura > 40) {
    console.log("calor dos infernos");  // Se a temperatura for maior que 40
} else if (temperatura > 30) {
    console.log("clima bom para pegar uma praia");  // Se a temperatura for maior que 30, mas menor ou igual a 40
} else {
    console.log("Abaixo de 30 ja estou de cachecou ");  // Se a temperatura for menor ou igual a 30
}